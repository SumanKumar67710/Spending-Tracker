   private void displayCategory()
    {
        try
        {
            category.removeAllItems();
             ResultSet rs=db.DbConnect.st.executeQuery(
                    "select * from category_info");
            while(rs.next())
            {
                category.addItem(rs.getString("category"));
            }
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
   private void getEntries()
   {
       try
       {
           javax.swing.table.DefaultTableModel dtm=
                   (javax.swing.table.DefaultTableModel)table.getModel();
           int rc=dtm.getRowCount();
           while(rc--!=0)
           {
               dtm.removeRow(0);
           }
           java.time.LocalDate cd=java.time.LocalDate.now();
           java.time.LocalDate bd=cd.minusDays(30);
           ResultSet rs=db.DbConnect.st.executeQuery(
                   "select * from spendings where sdate<='"+cd+"' and sdate>='"+bd+"'");
           int total=0;
           while(rs.next())
           {
               int t=rs.getInt("amount");
               total+=t;
         Object o[] ={rs.getDate("sdate"),rs.getString("category"),rs.getInt("amount")};
           dtm.addRow(o);
           }
           totalamount.setText(total+"");
       }
       catch(Exception ex)
       {
           JOptionPane.showMessageDialog(null, ex);
       }
   }
