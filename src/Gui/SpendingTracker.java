    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try
        {
            java.util.Date dt=d.getDate();
            String s1=a.getText();
            String c=(String)category.getSelectedItem();
            if(dt!=null && !s1.equals("") && !c.equals(""))
            {
               
                int amount=Integer.parseInt(s1);
                java.sql.Date date=new java.sql.Date(dt.getTime());
                db.DbConnect.st.executeUpdate(
                        "insert into spendings ( sdate,category,amount) values('"
                                
                                +date+"','"+c+"',"+amount+")"); 
                                
                 JOptionPane.showMessageDialog(null, "Expense Added Successfully");
                  getEntries();   
            }else
            {
               JOptionPane.showMessageDialog(null, "Plz fill all details");  
            }
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

